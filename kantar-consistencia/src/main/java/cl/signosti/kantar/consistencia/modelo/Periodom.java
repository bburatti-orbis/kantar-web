package cl.signosti.kantar.consistencia.modelo;

public class Periodom {
	private java.lang.Integer id;
	private java.lang.String periodo;
	private java.lang.Integer paisId;
	private java.lang.String dirName;
	private java.lang.Integer year;
	private java.lang.Integer month;
	
	public java.lang.Integer getId() {
		return id;
	}
	public void setId(java.lang.Integer id) {
		this.id = id;
	}
	public java.lang.String getPeriodo() {
		return periodo;
	}
	public void setPeriodo(java.lang.String periodo) {
		this.periodo = periodo;
	}
	public java.lang.Integer getPaisId() {
		return paisId;
	}
	public void setPaisId(java.lang.Integer paisId) {
		this.paisId = paisId;
	}
	public java.lang.String getDirName() {
		return dirName;
	}
	public void setDirName(java.lang.String dirName) {
		this.dirName = dirName;
	}
	public java.lang.Integer getYear() {
		return year;
	}
	public void setYear(java.lang.Integer year) {
		this.year = year;
	}
	public java.lang.Integer getMonth() {
		return month;
	}
	public void setMonth(java.lang.Integer month) {
		this.month = month;
	}
}
