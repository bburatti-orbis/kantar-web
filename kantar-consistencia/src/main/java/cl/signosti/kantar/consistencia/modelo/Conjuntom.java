package cl.signosti.kantar.consistencia.modelo;

public class Conjuntom {
	
	String nombre;
	int id;
	
	
	public Conjuntom(String nombre) {
		super();
		this.nombre = nombre;
	}
	public Conjuntom() {

	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	

}
